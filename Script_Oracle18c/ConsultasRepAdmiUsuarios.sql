FROM EQUIPO
JOIN SUBS_EQUIPOS ON SUBS_EQUIPOS.EQUIPO_ID_EQUIPO = EQUIPO.ID_EQUIPO 
JOIN USUARIOS ON USUARIOS.ID = SUBS_EQUIPOS.USUARIOS_ID 
WHERE EQUIPO.NOMBRE = 'Equipo1';


