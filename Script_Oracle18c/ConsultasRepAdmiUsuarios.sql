SELECT USUARIOS.NOMBRE, USUARIOS.APELLIDO, trunc(months_between(to_date(to_char(SYSDATE, 'dd/mm/yyyy'), 'dd/mm/yyyy'), to_date(to_char(USUARIOS.FECHA_NACIMIENTO , 'dd/mm/yyyy'), 'dd/mm/yyyy'))/12) AS EDAD
FROM USUARIOS
WHERE trunc(months_between(to_date(to_char(SYSDATE, 'dd/mm/yyyy'), 'dd/mm/yyyy'), to_date(to_char(USUARIOS.FECHA_NACIMIENTO , 'dd/mm/yyyy'), 'dd/mm/yyyy'))/12)>=20;

