 SELECT EMPLEADOS.NOMBRE, EMPLEADOS.APELLIDO, COUNT(NOTICIAS.EMPLEADOS_ID_EMPLEADO) AS CANT
 FROM NOTICIAS, EMPLEADOS
 WHERE EMPLEADOS.ID_EMPLEADO=NOTICIAS.EMPLEADOS_ID_EMPLEADO
 GROUP BY EMPLEADOS.NOMBRE, EMPLEADOS.APELLIDO 
 ORDER BY COUNT(NOTICIAS.EMPLEADOS_ID_EMPLEADO) DESC;

 SELECT EMPLEADOS.NOMBRE, EMPLEADOS.APELLIDO, COUNT(NOTICIAS.EMPLEADOS_ID_EMPLEADO) AS CANT
 FROM NOTICIAS, EMPLEADOS
