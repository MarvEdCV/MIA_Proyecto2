 WHERE EMPLEADOS.ID_EMPLEADO=NOTICIAS.EMPLEADOS_ID_EMPLEADO AND EQUIPO.NOMBRE = 'Equipo1'
 GROUP BY EMPLEADOS.NOMBRE, EMPLEADOS.APELLIDO, EQUIPO.NOMBRE  
